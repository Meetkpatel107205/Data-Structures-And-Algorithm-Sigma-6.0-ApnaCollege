
public class VariablesInJava {
    public static void main(String args[])
    {
        int a = 10;
        int b = 5;

        String name = "Tony Stark";

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("name = " + name);

        a = 50;
        b = a;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
    }    
}
