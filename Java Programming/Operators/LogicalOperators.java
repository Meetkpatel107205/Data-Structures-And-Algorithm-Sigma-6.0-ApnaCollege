
public class LogicalOperators {
    public static void main(String[] args) {

        System.out.println("\n---: Logical AND Operator :---\n");

        System.out.println("(3 < 5) && (10 > 5) : " + ((3 < 5) && (10 > 5)));

        System.out.println("\n---: Logical OR Operator :---\n");

        System.out.println("(3 < 5) || (5 > 10) : " + ((3 < 5) || (5 > 10)));

        System.out.println("\n---: Logical NOT Operator :---\n");

        System.out.println("!(3 > 5) : " + (!(3 > 5)));
    }    
}
