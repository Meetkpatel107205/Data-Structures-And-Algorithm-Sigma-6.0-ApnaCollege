
public class AssignmentOperators {
    public static void main(String[] args) {

        int A = 10;
        // A = A + 10;
        A += 10; // ---> Short hand Assignment Operator

        int B = 5;
        // B = B - 5;
        B -= 5;

        int C = 5;
        // C = C * 5;
        C *= 5;

        int D = 5;
        // D = D / 5;
        D /= 5;

        System.out.println("A = " + A);
        System.out.println("B = " + B);
        System.out.println("C = " + C);
        System.out.println("D = " + D);
    }
}
